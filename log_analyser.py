# ─── Color helpers ───────────────────────────────────────────────────────────
RED    = "\033[91m"
GREEN  = "\033[92m"
YELLOW = "\033[93m"
CYAN   = "\033[96m"
BOLD   = "\033[1m"
DIM    = "\033[2m"
RESET  = "\033[0m"

def color(text, c):
    return f"{c}{text}{RESET}"

def section(icon, title, body):
    print(f"\n  {icon} {color(title, BOLD+CYAN)}")
    for line in (body if isinstance(body, list) else body.split("\n")):
        print(f"     {line}")

