# ─── Offline analysis engine ─────────────────────────────────────────────────
def analyze_offline(logs):
    """Match logs against known patterns and return structured results."""
    matches = []
    seen = set()
    for pat in PATTERNS:
        if re.search(pat["regex"], logs) and pat["summary"] not in seen:
            matches.append(pat)
            seen.add(pat["summary"])
    return matches

def print_analysis(matches, logs):
    """Pretty-print the analysis results."""
    if not matches:
        print(color("\n  ⚠️  No known patterns matched.", YELLOW))
        print("     Try pasting a more complete log, or set OPENAI_API_KEY for AI-powered analysis.\n")
        return

    print(f"\n  {color('━' * 60, DIM)}")
    print(f"  {color(f'  Found {len(matches)} issue(s)', BOLD+GREEN)}")
    print(f"  {color('━' * 60, DIM)}")

    for i, m in enumerate(matches, 1):
        sev = m["severity"]
        sev_color = RED if sev == "High" else YELLOW if sev == "Medium" else GREEN
        print(f"\n  {color(f'── Issue {i} ──', BOLD)}")

        section("📋", "Summary",    m["summary"])
        section("🔎", "Root Cause", m["cause"])
        section("💥", "Impact",     m.get("impact", "See summary above."))
        section("🏷️ ", "Severity",   color(sev, sev_color + BOLD))
        section("🛠️ ", "Fix Steps",  m["fix"])

    print(f"\n  {color('━' * 60, DIM)}\n")

# ─── Optional: OpenAI-powered deep analysis ──────────────────────────────────
def analyze_with_openai(logs):
    """Use OpenAI GPT for deeper analysis if OPENAI_API_KEY is set."""
    try:
        from openai import OpenAI
        api_key = os.environ.get("OPENAI_API_KEY")
        if not api_key:
            return None

        client = OpenAI(api_key=api_key)
        prompt = f"""You are a senior SRE engineer helping a developer understand an error log.

Analyze the following logs and provide (in plain, simple language):
1. **Error Summary** — one-line plain English summary
2. **Root Cause** — simple explanation anyone can understand
3. **Impact** — what breaks because of this?
4. **Fix Steps** — numbered, copy-paste-ready commands where possible
5. **Severity** — Low / Medium / High

Logs:
{logs}
"""
        response = client.chat.completions.create(
            model="gpt-4o-mini",
            messages=[{"role": "user", "content": prompt}],
            temperature=0.3,
        )
        return response.choices[0].message.content
    except Exception:
        return None

# ─── Main ─────────────────────────────────────────────────────────────────────
def run_analysis(logs):
    """Run the analysis on the given log text."""
    matches = analyze_offline(logs)
    print_analysis(matches, logs)

    # Optional: OpenAI deep analysis (if key is set)
    api_key = os.environ.get("OPENAI_API_KEY")
    if api_key:
        print(color("  🤖 Running AI deep analysis...\n", DIM))
        ai_result = analyze_with_openai(logs)
        if ai_result:
            section("🤖", "AI Deep Analysis", ai_result)
            print()
    else:
        print(color("  💡 Tip: export OPENAI_API_KEY='...' for AI-powered deep analysis.\n", DIM))


def main():
    import argparse
    parser = argparse.ArgumentParser(description="🔍 AI Log Analyzer — paste or pass a file to analyze error logs.")
    parser.add_argument("file", nargs="?", help="Path to a log file to analyze (avoids terminal paste limits)")
    args = parser.parse_args()

    banner = f"""
{color('╔══════════════════════════════════════════════════════════════╗', CYAN)}
{color('║', CYAN)}   🔍  {color('AI Log Analyzer', BOLD)}                                      {color('║', CYAN)}
{color('╚══════════════════════════════════════════════════════════════╝', CYAN)}"""

    # ── Mode 1: File argument  (python3 log_analyser.py error.log)
    if args.file:
        print(banner)
        path = args.file
        if not os.path.isfile(path):
            print(color(f"\n  ❌ File not found: {path}\n", RED))
            sys.exit(1)
        with open(path, "r") as f:
            logs = f.read().strip()
        if not logs:
            print(color("\n  ⚠️  File is empty.\n", YELLOW))
            sys.exit(1)
        print(color(f"\n  📂 Reading from: {path}  ({len(logs)} chars)\n", DIM))
        run_analysis(logs)
        return

    # ── Mode 2: Piped input  (cat error.log | python3 log_analyser.py)
    if not sys.stdin.isatty():
        print(banner)
        logs = sys.stdin.read().strip()
        if logs:
            run_analysis(logs)
        return

    # ── Mode 3: Interactive  (python3 log_analyser.py)
    print(f"""
{color('╔══════════════════════════════════════════════════════════════╗', CYAN)}
{color('║', CYAN)}   🔍  {color('AI Log Analyzer', BOLD)}                                      {color('║', CYAN)}
{color('║', CYAN)}                                                              {color('║', CYAN)}
{color('║', CYAN)}   {color('Option A:', BOLD)} Save log to a file, then run:                  {color('║', CYAN)}
{color('║', CYAN)}     {color('python3 log_analyser.py error.log', GREEN)}                      {color('║', CYAN)}
{color('║', CYAN)}                                                              {color('║', CYAN)}
{color('║', CYAN)}   {color('Option B:', BOLD)} Pipe it in:                                    {color('║', CYAN)}
{color('║', CYAN)}     {color('cat error.log | python3 log_analyser.py', GREEN)}                {color('║', CYAN)}
{color('║', CYAN)}                                                              {color('║', CYAN)}
{color('║', CYAN)}   {color('Option C:', BOLD)} Paste below and press {color('Enter', BOLD)}.                    {color('║', CYAN)}
{color('║', CYAN)}     (Terminal may truncate long pastes — use A or B instead)  {color('║', CYAN)}
{color('║', CYAN)}                                                              {color('║', CYAN)}
{color('║', CYAN)}   Type {color('quit', BOLD)} or press {color('Ctrl+C', BOLD)} to exit.                        {color('║', CYAN)}
{color('╚══════════════════════════════════════════════════════════════╝', CYAN)}
""")

    while True:
        print(color("  Paste log ↓\n", DIM))
        lines = []
        try:
            # Read first line (required)
            first = input()
            if first.lower() in ("quit", "exit", "q"):
                print(color("\n  👋 Bye!\n", CYAN))
                sys.exit(0)
            if first.strip() == "":
                continue  # skip blank input
            lines.append(first)

            # If the first line is long (>100 chars), it's likely a full log on one line
            # Analyze immediately after Enter
            if len(first) > 100:
                pass  # skip multi-line collection, analyze now
            else:
                # For short first lines, collect more lines until blank line
                while True:
                    line = input()
                    if line == "":
                        break  # blank line = done
                    lines.append(line)

        except EOFError:
            pass  # Ctrl+D — submit what we have
        except KeyboardInterrupt:
            print(color("\n\n  👋 Bye!\n", CYAN))
            sys.exit(0)

        if not lines:
            continue

        logs = "\n".join(lines)
        run_analysis(logs)

        print(color("  ─── Ready for next log. Type 'quit' to exit. ───\n", DIM))


if __name__ == "__main__":
    main()
